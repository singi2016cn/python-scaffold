# 人生的不同阶段

human_age = 4
if human_age < 2:
    print('你是婴儿')
elif human_age >= 2 and human_age < 4:
    print('你正在蹒跚学步')
elif human_age >= 4 and human_age < 13:
    print('你是儿童')
elif human_age >= 13 and human_age < 20:
    print('你是青少年')
elif human_age >= 20 and human_age < 65:
    print('你是成年人')
elif human_age >= 65:
    print('你是老年人')
